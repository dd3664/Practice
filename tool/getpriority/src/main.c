int main(int argc, char *argv[])
{	
	int pid;
	int ret;

	if (argc < 2)
	{
		printf("Please input the pid\n");
		return 0;
	}

	pid = atoi(argv[1]);
	ret = getpriority(PRIO_PROCESS, pid);
	printf("Priority of pid:%d = %d\n", pid, ret);
	return 0;
}
