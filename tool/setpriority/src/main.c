int main(int argc, char *argv[])
{	
	int pid;
	int nice;
	int ret;

	if (argc < 3)
	{
		printf("Please input the pid and nice\n");
		return 0;
	}

	pid = atoi(argv[1]);
	nice = atoi(argv[2]);
	nice = nice > 19 ? 19 : nice;
	nice = nice < -20 ? -20 : nice;
	ret = setpriority(PRIO_PROCESS, pid, nice);
	if (0 == ret)
	{
		printf("Set priority of pid:%d = %d\n", pid, nice);
	}
	else
	{
		printf("Set priority of pid:%d faild\n", pid);
	}
	return 0;
}
