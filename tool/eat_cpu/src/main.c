int main(int argc, char *argv[])
{	
	int id = 0;
	int a = 0;
	long double b = 0.000001;
	int count = 0;

	if (NULL == argv[1])
	{
		printf("Please input an param as id\n");
		return 0;
	}
	id = atoi(argv[1]);
	printf("============Program %d Start============\n", id);
	while(1)
	{
		a++;
		b = b * b + 1;
		if (a == 1000)
		{
			//printf("a = %d\n", a);
			count++;
			a = 0;
		}
		if (count == 10000)
		{
			printf("id = %d, count = %d\n",id,  count);
			count = 0;
		}
	}
	return 0;
}
