int main()
{
	DListNode *head = NULL;
	DListNode *tmp = NULL;
	head = DListCreate();
	DListPushBack(head, 1);
	DListPushBack(head, 2);
	DListPushBack(head, 3);
	DListPrint(head);
	DListPushFront(head, 4);
	DListPushFront(head, 5);
	DListPrint(head);
	DListPopBack(head);
	DListPrint(head);
	DListPopFront(head);
	DListPrint(head);
	tmp = DListFind(head, 1);
	DListInsert(tmp, 0);
	DListPrint(head);
	DListErase(tmp);
	DListPrint(head);
	DListDestroy(&head);
	DListPrint(head);
    return 0;
}
