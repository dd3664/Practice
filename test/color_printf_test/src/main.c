/****************************************************************************************************/
/*                                           INCLUDE                                                */
/****************************************************************************************************/
#include <stdio.h>

/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/
#define RESET     "\033[0m"
#define RED       "\033[31m"
#define GREEN     "\033[32m"
#define YELLOW    "\033[33m"
#define BLUE      "\033[34m"
#define MAGENTA   "\033[35m"
#define CYAN      "\033[36m"
#define WHITE     "\033[37m"

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/
int main(int argc, char *argv[])
{
	printf(RED "This is red text\n" RESET);
	printf(GREEN "This is a green test\n" RESET);
	printf(YELLOW "This is a yellow text\n" RESET);
	printf(BLUE "This is blue text\n" RESET);
	printf(MAGENTA "This is a megenta text\n" RESET);
	printf(CYAN "This is a cyan text\n" RESET);
	printf(WHITE "This is a white text\n" RESET);
	return 0;
}
