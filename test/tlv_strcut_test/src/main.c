/****************************************************************************************************/
/*                                           INCLUDE                                                */
/****************************************************************************************************/
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/
typedef unsigned char UINT8;

typedef struct _tlv_struct {
	UINT8 type;
	UINT8 len;
	UINT8 value[0];
} TLV_STRUCT;

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/
int main(int argc, char *argv[])
{	
	TLV_STRUCT *test = (TLV_STRUCT *)malloc(sizeof(TLV_STRUCT) + 10);
	memset(test, 0, sizeof(TLV_STRUCT) + 10);
	test->type = 1;
	test->len = 10;
	memcpy(test->value, "123456789\0", test->len);
	printf("sizeof TLV_STRUCT=%lu, test->value=%s\n", sizeof(TLV_STRUCT), test->value);
	free(test);
	return 0;
}
