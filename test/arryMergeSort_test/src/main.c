#include <stdio.h>
#include <stdlib.h>
/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/
#define MAX_LEN 1000

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/
int tmp[MAX_LEN];

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/

void mergeSort(int array[], int left, int right)
{
	int mid = (left + right) >> 1;
	int k = 0;
	int i = left;
	int j = mid + 1;

	if (left >= right)
		return; //特判区间内如果只有一个数或者为空时，直接return;

	mergeSort(array, left, mid);
	mergeSort(array, mid + 1, right); //递归排序两边
	
	while (i <= mid && j <= right) //归并，合并两边
	{
		if (array[i] <= array[j])
			tmp[k++] = array[i++];
		else
			tmp[k++] = array[j++];
	}

	while (i <= mid) //再次查看左边区间是否还有剩余
	{
		tmp[k++] = array[i++];
	}

	while (j <= right) //再次查看右边区间是否还有剩余
	{
		tmp[k++] = array[j++];
	}

	for (i = left, j = 0; i <= right; i++, j++) //将tmp[i]存回array[i]之中
	{
		array[i] = tmp[j];
	}
}

int main ()
{
	int n = 0;
	int array[1000] = {0};
	printf("请输入数组个数\n");
    scanf("%d", &n);
    for(int i = 0; i < n; i++)
	{
		printf("请输入第%d个数值\n", i + 1);
		scanf("%d", &array[i]);
	}
    mergeSort(array, 0, n - 1);
    for(int i = 0; i < n; i++)
	{
		printf("%d ", array[i]);
	}
    return 0;
}