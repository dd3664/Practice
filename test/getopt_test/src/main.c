#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <getopt.h>
/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/
int main(int argc, char *argv[])
{	
	int opt;
	const char opt_set[] = "ab:c:";
	while ((opt = getopt(argc, argv, opt_set)) != -1)
	{
		switch (opt)
		{
			case 'a':
				printf("Option a\n");
				break;
			case 'b':
				printf("Option b with value: %s\n", optarg);
				break;
			case 'c':
				printf("Option c with value: %s\n", optarg);
				break;
			default:
				printf("Usage: %s [-a] [-b value] [-c value]\n", argv[0]);
				break;
		}
	}
	return 0;
}
