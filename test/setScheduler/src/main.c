/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/
int main(int argc, char *argv[])
{	
	int pid = atoi(argv[1]);
	int policy = atoi(argv[2]);
	int sched_priority = argv[3] ? atoi(argv[3]) : 1;
	int ret = 0;
	struct sched_param param = {0};
	if (policy == SCHED_OTHER)
		param.sched_priority = 0;
	else
		param.sched_priority = sched_priority;
	ret = sched_setscheduler(pid, policy, &param);
	printf("SCHED_OTHER=%d, SCHED_FIFO=%d, SCHED_RR=%d\n", SCHED_OTHER, SCHED_FIFO, SCHED_RR);
	printf("pid=%d, ret=%d\n", pid, ret);
	return 0;
}
