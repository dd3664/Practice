#include <stdio.h>
#include <stdlib.h>
/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/
#define MAX_LEN 1000

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/
int tmp[MAX_LEN];

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/

/*
@function: 归并排序
@params: array[], 待排序数组
@params: left, 数组起始下标
@params: right, 数组结束下标
*/
void mergeSort(int array[], int left, int right)
{
	int mid = (left + right) >> 1;
	int k = 0;
	int i = left;
	int j = mid + 1;

	if (left >= right)
		return; //特判区间内如果只有一个数或者为空时，直接return;

	mergeSort(array, left, mid);
	mergeSort(array, mid + 1, right); //递归排序两边
	
	while (i <= mid && j <= right) //归并，合并两边
	{
		if (array[i] <= array[j])
			tmp[k++] = array[i++];
		else
			tmp[k++] = array[j++];
	}

	while (i <= mid) //再次查看左边区间是否还有剩余
	{
		tmp[k++] = array[i++];
	}

	while (j <= right) //再次查看右边区间是否还有剩余
	{
		tmp[k++] = array[j++];
	}

	for (i = left, j = 0; i <= right; i++, j++) //将tmp[i]存回array[i]之中
	{
		array[i] = tmp[j];
	}
}