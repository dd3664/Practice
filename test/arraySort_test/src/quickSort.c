/****************************************************************************************************/
static void swap(int *a, int *b)
{
	int tmp = *a;
	*a = *b;
	*b = tmp;
}
/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/

/*
@function: 快速排序
@params: array[], 待排序数组
@params: left, 数组起始下标
@params: right, 数组结束下标
*/
void quickSort(int array[], int left, int right)
{
	int mid = (left + right) >> 1;
	int i = left;
	int j = right;
	int key = 0;

	if (left >= right)
		return; //特判区间内如果只有一个数或者为空时，直接return;

	key = array[mid]; //以中间值作为快排基准值
	while (i < j)
	{
		while (array[i] < key)
			i++;
		while (array[j] > key)
			j--;
		if (i < j)
		{
			swap(&array[i], &array[j]);
		}		
	}

	quickSort(array, left, j);
	quickSort(array, j + 1, right);
}