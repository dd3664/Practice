/*--------------------------------------------------------------------------------STATIC FUNCTIONS--------------------------------------------------------------------------------*/
static int add(int a, int b)
{
   printf("Entry of func: add\n");
   return a + b;
}

static int substract(int a, int b)
{
   printf("Entry of func: substract\n");
   return a - b;
}
/*--------------------------------------------------------------------------------PUBLIC FUNCTIONS--------------------------------------------------------------------------------*/

__attribute__((constructor)) void testModuleInit()
{
   regCallback("add", add);
   regCallback("substract", substract);
}
