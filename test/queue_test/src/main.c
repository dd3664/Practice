#include <stdio.h>
#include <stdlib.h>
#include "seqQueue.h"

/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/
seqQueue l_queue;
/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
/****************************************************************************************************/
int main(int argc, char *argv[])
{
	int i;
	int num;

	initSeqQueue(&l_queue);
	if(isSeqQueueEmpty(&l_queue))
		printf("queue is empty\n");
	enSeqQueue(&l_queue, 1);
	enSeqQueue(&l_queue, 2);
	enSeqQueue(&l_queue, 3);
	enSeqQueue(&l_queue, 7);
	enSeqQueue(&l_queue, 9);
	enSeqQueue(&l_queue, 10);
	
	getSeqQueueHead(&l_queue, &i);
	printf("head of queue:%d\n", i);

	deSeqQueue(&l_queue, &i);
	printf("element1:%d\n", i);

	deSeqQueue(&l_queue, &i);
	printf("element2:%d\n", i);

	deSeqQueue(&l_queue, &i);
	printf("element3:%d\n", i);

	num = getSeqQueueNum(&l_queue);
	printf("number of queue:%d\n", num);
	return 0;
}
