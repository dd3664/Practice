int reverse(int x){
    int reversed = 0;
    int tmp = 0;

    while (x)
    {   
        tmp = x % 10;
        if (reversed > 214748364 || (reversed == 214748364 && tmp > 7))
        {
            return 0;
        }
        if (reversed < -214748364 || (reversed == -214748364 && tmp < -8))
        {
            return 0;
        }
        reversed = reversed * 10 + tmp;
        x = x / 10;
    }

    return reversed;
}

int main(int argc, char *argv[])
{
	int a;
	int b;
	a = 321; /* 正数 */
	b = reverse(a);
	printf("a = %d, b = %d\n", a, b);
	a = -123; /* 负数 */
	b = reverse(a);
	printf("a = %d, b = %d\n", a, b);
	a = 1147483646; /* 反转后溢出 */
	b = reverse(a);
	printf("a = %d, b = %d\n", a, b);
	return 0;
}
