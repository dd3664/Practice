int myAtoi(char* s) {
    int ret = 0;
    int sign_flag = 1;
    int i = 0;

    while(s[i] == ' ')
    {
        i++;
    }

    if (s[i] == '-')
    {
        sign_flag = -1;
		i++;
    }
	else if (s[i] == '+')
	{
		sign_flag = 1;
		i++;
	}

    while(s[i] == '0')
	{
		i++;
	}

	while(s[i] >= '0' && s[i] <= '9')
	{
		if ((1 == sign_flag) && ((ret > SIGNED_INT_MAX / 10) || ((ret == SIGNED_INT_MAX / 10) && (s[i] - '0') >= SIGNED_INT_MAX % 10)))
		{
			return SIGNED_INT_MAX;
		}
		if ((-1 == sign_flag) && ((ret > SIGNED_INT_MIN / 10) || ((ret == SIGNED_INT_MIN / 10) && (s[i] - '0') >= SIGNED_INT_MIN % 10)))
		{
			return SIGNED_INT_MIN;
		}
		ret = (ret * 10 + (s[i] - '0'));
		i++;
	}
	ret = sign_flag * ret;
	return ret;
}

int main(int argc, char *argv[])
{
	char *s = argv[1];
	int ret = 0;
	ret = myAtoi(s); 
	printf("ret=%d\n", ret);	
	return 0;
}
