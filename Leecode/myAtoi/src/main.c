/****************************************************************************************************/
/*                                           DEFINES                                                */
/****************************************************************************************************/
#define SIGNED_INT_MAX	 0x7FFFFFFF
#define SIGNED_INT_MIN  0x80000000
/****************************************************************************************************/
/*                                           VARIABLES                                              */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       STATIC FUNCTIONS                                           */
/****************************************************************************************************/

/****************************************************************************************************/
/*                                       PUBLIC FUNCTIONS                                           */
